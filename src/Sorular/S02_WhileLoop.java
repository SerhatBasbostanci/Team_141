package Sorular;

public class S02_WhileLoop {

    // Java String' ini "J*a*v*a*" String'ine ceviren kodu yazdiriniz
    // sonucu consolda gosterin


    public static void main(String[] args) {


        String str = "Java";
        int i = 0;

        while (i < str.length ()){
            System.out.println (str.charAt ( i )+"*");

            i++;
        }











    }


}
